-- Helper: ¿es admin?
create or replace function public.is_admin()
returns boolean language sql stable security definer set search_path = public as $$
  select exists (select 1 from public.profiles where id = auth.uid() and role = 'administrador');
$$;

-- PROFILES: cada quien ve/edita su perfil; admin todo
create policy "perfil propio: leer"   on public.profiles for select using (auth.uid() = id or public.is_admin());
create policy "perfil propio: editar" on public.profiles for update using (auth.uid() = id);

-- PETS: el dueño gestiona las suyas; lectura pública (página por QR); admin todo
create policy "mascotas: lectura pública" on public.pets for select using (true);
create policy "mascotas: dueño inserta"   on public.pets for insert with check (auth.uid() = owner_id);
create policy "mascotas: dueño edita"     on public.pets for update using (auth.uid() = owner_id or public.is_admin());
create policy "mascotas: dueño elimina"   on public.pets for delete using (auth.uid() = owner_id or public.is_admin());

-- CONTACTOS: lectura pública (para mascota perdida); gestiona el dueño de la mascota
create policy "contactos: lectura pública" on public.pet_contacts for select using (true);
create policy "contactos: dueño gestiona"  on public.pet_contacts for all
  using (exists (select 1 from public.pets p where p.id = pet_id and p.owner_id = auth.uid()))
  with check (exists (select 1 from public.pets p where p.id = pet_id and p.owner_id = auth.uid()));

-- HISTORIAL / VACUNAS / DOCUMENTOS: solo el dueño (privado)
create policy "historial: dueño" on public.medical_records for all
  using (exists (select 1 from public.pets p where p.id = pet_id and p.owner_id = auth.uid()) or public.is_admin())
  with check (exists (select 1 from public.pets p where p.id = pet_id and p.owner_id = auth.uid()));
create policy "vacunas: dueño" on public.vaccines for all
  using (exists (select 1 from public.pets p where p.id = pet_id and p.owner_id = auth.uid()) or public.is_admin())
  with check (exists (select 1 from public.pets p where p.id = pet_id and p.owner_id = auth.uid()));
create policy "documentos: dueño" on public.documents for all
  using (exists (select 1 from public.pets p where p.id = pet_id and p.owner_id = auth.uid()) or public.is_admin())
  with check (exists (select 1 from public.pets p where p.id = pet_id and p.owner_id = auth.uid()));

-- RECORDATORIOS: del usuario
create policy "recordatorios: propio" on public.reminders for all
  using (auth.uid() = user_id) with check (auth.uid() = user_id);

-- REGISTROS: el usuario ve los suyos; admin gestiona (confirma pagos)
create policy "registros: propio leer" on public.registrations for select using (auth.uid() = user_id or public.is_admin());
create policy "registros: propio crea" on public.registrations for insert with check (auth.uid() = user_id);
create policy "registros: admin edita" on public.registrations for update using (public.is_admin());
