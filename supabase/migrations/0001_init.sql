create extension if not exists unaccent;

-- ----- Helper: slugify -----
create or replace function public.slugify(v text)
returns text language sql as $$
  select trim(both '-' from regexp_replace(lower(unaccent(coalesce(v, ''))), '[^a-z0-9]+', '-', 'g'));
$$;

-- =========================================================================
--  PERFILES (extiende auth.users)
-- =========================================================================
create table if not exists public.profiles (
  id               uuid primary key references auth.users(id) on delete cascade,
  role             text not null default 'propietario'
                   check (role in ('propietario','veterinario','empresa','administrador')),
  nombre           text not null default '',
  apellido         text not null default '',
  celular          text,
  tipo_documento   text,
  numero_documento text,
  direccion        text,
  distrito         text,
  ciudad           text,
  fecha_nacimiento date,
  avatar_url       text,
  created_at       timestamptz not null default now()
);

-- Crear perfil automáticamente cuando se registra un usuario
create or replace function public.handle_new_user()
returns trigger language plpgsql security definer set search_path = public as $$
begin
  insert into public.profiles (id, role, nombre, apellido, celular, tipo_documento, numero_documento)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'role', 'propietario'),
    coalesce(new.raw_user_meta_data->>'nombre', ''),
    coalesce(new.raw_user_meta_data->>'apellido', ''),
    new.raw_user_meta_data->>'celular',
    new.raw_user_meta_data->>'tipo_documento',
    new.raw_user_meta_data->>'numero_documento'
  );
  return new;
end; $$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

-- =========================================================================
--  MASCOTAS
-- =========================================================================
create sequence if not exists public.pet_code_seq;

create table if not exists public.pets (
  id                  bigint generated by default as identity primary key,
  owner_id            uuid not null references public.profiles(id) on delete cascade,
  identipet_code      text unique,
  slug_publico        text unique,
  codigo_interno      text,
  foto_url            text,
  nombre              text not null,
  especie             text not null default 'Perro',
  raza                text,
  sexo                text not null default 'desconocido' check (sexo in ('macho','hembra','desconocido')),
  fecha_nacimiento    date,
  color               text,
  peso                numeric(6,2),
  tiene_microchip     boolean not null default false,
  numero_chip         text,
  caracteristicas     text,
  estado_reproductivo text not null default 'desconocido'
                      check (estado_reproductivo in ('entero','castrado','esterilizado','desconocido')),
  perdido             boolean not null default false,
  perdido_desde       timestamptz,
  recompensa          text,
  estado              text not null default 'activa' check (estado in ('activa','inactiva','fallecida')),
  pago_estado         text not null default 'pendiente' check (pago_estado in ('pendiente','pagado','anulado')),
  created_at          timestamptz not null default now(),
  updated_at          timestamptz not null default now()
);
create index if not exists idx_pets_owner on public.pets(owner_id);

-- Generar número IdentiPet, slug y código interno al insertar
create or replace function public.set_pet_identifiers()
returns trigger language plpgsql as $$
declare seqval bigint;
begin
  if new.identipet_code is null then
    seqval := nextval('public.pet_code_seq');
    new.identipet_code := 'IP-' || lpad(seqval::text, 6, '0');
    new.slug_publico := public.slugify(new.nombre) || '-' || seqval::text;
    new.codigo_interno := upper(substr(md5(random()::text), 1, 10));
  end if;
  return new;
end; $$;

drop trigger if exists trg_pet_identifiers on public.pets;
create trigger trg_pet_identifiers
  before insert on public.pets
  for each row execute function public.set_pet_identifiers();

